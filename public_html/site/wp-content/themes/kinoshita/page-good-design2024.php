  // スライダー.
  const sliderTrack = document.querySelector('.slider-track');
  const slides = document.querySelectorAll('.slide');
  const slideGap = 20;
  const slideWidth = slides[0].offsetWidth + slideGap;
  const totalSlides = slides.length;

  // 初期設定
  let offset = 0;

  // 無限ループのため、スライドを複製
  for (let i = 0; i < totalSlides; i++) {
    const clone = slides[i].cloneNode(true);
    sliderTrack.appendChild(clone);
  }

  // トラックを動かし続ける関数
  function continuousSlide() {
    // スライダーを移動
    offset -= 1; // 1pxずつ移動
    sliderTrack.style.transform = `translateX(${offset}px)`;

    // 無限ループのために位置をリセット
    if (Math.abs(offset) >= slideWidth * totalSlides) {
      offset = 0; // 最初に戻る
      sliderTrack.style.transition = 'none'; // トランジションを無効化して瞬時に戻る
      sliderTrack.style.transform = `translateX(${offset}px)`;
      // 次フレームでトランジションを復活.
      requestAnimationFrame(() => {
        sliderTrack.style.transition = 'transform 0.2s linear';
      });
    }

    // 次のフレームを要求.
    requestAnimationFrame(continuousSlide);
  }

  // トランジションの設定.
  sliderTrack.style.transition = 'transform 0.2s linear';

  // スライドを開始.
  continuousSlide();

  // もっと見るボタン.
  const moreBtns = document.querySelectorAll('.more-btn');
  moreBtns.forEach(btn => {
    btn.addEventListener('click', () => {
      const awardDetail = btn.nextElementSibling;
      awardDetail.classList.toggle('is-active');

      // ボタン自身を非表示にする
      btn.style.display = 'none';
    });
  });

  document.addEventListener('DOMContentLoaded', () => {
    const fadeElements = document.querySelectorAll('.fadeup');

    const observer = new IntersectionObserver(entries => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add('fade-on');
        }
      });
    });

    fadeElements.forEach(el => observer.observe(el));
  });
</script>
